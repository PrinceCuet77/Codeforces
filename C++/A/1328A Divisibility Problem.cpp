#include <bits/stdc++.h>
using namespace std;

typedef long long ll; 

void solve() {
        ll a, b; 
        while ( cin >> a >> b ) {
                if ( a % b == 0 ) {
                        cout << "0\n";
                        return;
                }

                ll t = a % b; 
                cout << abs(b - t) << "\n";
        }
}

int main()
{
        ios_base :: sync_with_stdio(0);
        cin.tie(0);
        cout.tie(0);

        // #ifndef prince77
        //         freopen("input.txt", "r", stdin);
        //         freopen("output.txt", "w", stdout);
        // #endif

        int tc;
        cin >> tc;
        while ( tc-- )
                solve();

        return 0;
}
